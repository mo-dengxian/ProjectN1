public class Demo004 {

    /*进制问题
    在java中通过前缀表示一个数的进制
    整数扩展：二进制0b  十进制   八进制0  十六进制0x
     */
    public static void main(String[] args){
        int i=0b010;
        int i1=10;
        int i2=010;
        int i3=0x10;
        System.out.println(i);
        System.out.println(i1);
        System.out.println(i2);
        System.out.println(i3);
        System.out.println("-----------------------");
        //浮点数扩展
    }

}
